package com.example.a10_26_2019_loginpage.manager;

public class ManagerDataSingleton {

    private String userID;
    private String name;
    private  String hometown;
    private String phone;
    private String doorNumber;
    private int dayOfBirth;
    private int monthOfBirth;
    private int yearOfBirth;

    private static ManagerDataSingleton INSTANCE;
    private ManagerDataSingleton(){

    }

    public static ManagerDataSingleton getInstance(){
        if(INSTANCE == null){
            INSTANCE = new ManagerDataSingleton();
        }

        return INSTANCE;
    }

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHometown() {
        return hometown;
    }

    public void setHometown(String hometown) {
        this.hometown = hometown;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getDoorNumber() {
        return doorNumber;
    }

    public void setDoorNumber(String doorNumber) {
        this.doorNumber = doorNumber;
    }

    public int getDayOfBirth() {
        return dayOfBirth;
    }

    public void setDayOfBirth(int dayOfBirth) {
        this.dayOfBirth = dayOfBirth;
    }

    public int getMonthOfBirth() {
        return monthOfBirth;
    }

    public void setMonthOfBirth(int monthOfBirth) {
        this.monthOfBirth = monthOfBirth;
    }

    public int getYearOfBirth() {
        return yearOfBirth;
    }

    public void setYearOfBirth(int yearOfBirth) {
        this.yearOfBirth = yearOfBirth;
    }

}
